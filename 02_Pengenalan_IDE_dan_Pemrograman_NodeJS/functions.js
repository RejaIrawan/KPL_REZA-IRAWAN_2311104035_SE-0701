// named function
function perkalian(a,b) {
    let result = a*b
    return result
}
// expresif function // arrow function

// anonymous function
const pertambahan = (a,b) => {
    let result = a+b
    return result
}
// named arrow function
const pembagian = function bagi(a,b){
    let result = a/b
    return result
}

console.log(perkalian(15,3))
console.log(pertambahan(7,6))
console.log(pembagian(10,2))